
def lista_2_ex_2():
    """
     Faça um Programa que peça um valor e mostre na tela se o valor é positivo ou negativo.
    """
    a = int(input("digite um número: "))

    if float(a) >= 0:
        print ("O número digitado é positivo" )
    else:
        print ("O número digitado é negativo")

